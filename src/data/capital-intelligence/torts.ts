import type { TortMetric, ScoreHistoryPoint, FilingPoint } from "./types";

function scoreHistory(base: number, drift: number): ScoreHistoryPoint[] {
  return Array.from({ length: 12 }, (_, i) => {
    const noise = Math.sin(i * 1.3) * 3 + Math.cos(i * 0.7) * 2;
    return {
      date: `M-${11 - i}`,
      score: Math.max(0, Math.min(100, Math.round(base - drift + (drift * i) / 11 + noise))),
    };
  });
}

function filingHistory(startBase: number, growth: number): FilingPoint[] {
  let cum = 0;
  return Array.from({ length: 12 }, (_, i) => {
    const noise = Math.sin(i * 0.9) * (startBase * 0.15);
    const filings = Math.max(20, Math.round(startBase + growth * i + noise));
    cum += filings;
    return { month: `M-${11 - i}`, filings, cumulative: cum };
  });
}

